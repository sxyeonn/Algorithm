-- RECURSIVE CTE
WITH RECURSIVE CTE AS(
    SELECT 0 AS NUM
    UNION ALL
    SELECT NUM +1
    FROM CTE
    WHERE NUM < 23
)
SELECT CTE.NUM HOUR, COUNT(HOUR(O.DATETIME)) AS COUNT 
-- COUNT(*)으로 하면 데이터 없는 ROW도 1로 CNT 하므로 COUNT(HOUR(O.DATETIME)) 사용
FROM ANIMAL_OUTS O RIGHT OUTER JOIN CTE
ON HOUR(O.DATETIME) = CTE.NUM
GROUP BY CTE.NUM
ORDER BY CTE.NUM